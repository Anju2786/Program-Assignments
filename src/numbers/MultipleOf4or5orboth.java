package numbers;

public class MultipleOf4or5orboth {
	public static void main (String[] args) {
		int i=1;
		while(i<=100)
		{
			
		
		if(i%4==0 && i%5==0)
		System.out.println("Lara");
		
		else if(i%4==0 )
			System.out.println("java");
		
		else if(i%5==0 )
			System.out.println("j2ee");
		
		
		
		else
			
		break;
		i++;
		
	}
	}

}
