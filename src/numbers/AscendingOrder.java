package numbers;

import java.util.Arrays;

public class AscendingOrder {
	 public static void main (String[] args) 
	    { 
	        int a[] = {10, 12, 5}; 
	  
	        Arrays.sort(a); 
	  
	        for (int i = 0; i < 3; i++)  
	            System.out.print( a[i] + " "); 
	    } 

}
